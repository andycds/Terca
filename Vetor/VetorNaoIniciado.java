public class VetorNaoIniciado {
    public static void main(String[] args) {
        /*
         * aqui mostramos somente a primeira posição de cada vetor
         * para simplificar, porém todas elas foram inicializadas
         */
        // vetor de ints
        int vi[] = new int[2];
        // vetor de float
        float vf[] = new float[2];
        // vetor de char
        char vc[] = new char[2];
        // vetor de String
        String vs[] = new String[2];
        // vetor de LivroDeNotas
        // LivroDeNotas vl[] = new LivroDeNotas[2];
        // vetor de Integer
        Integer vInteger[] = new Integer[2];
        System.out.println(vi[0]);
        System.out.println(vf[0]);
        System.out.println(vc[0]);
        System.out.println(vs[0]);
        // System.out.println(vl[0]);
        System.out.println(vInteger[0]);
    }
}